//Fitur Dasar Yang Ada Pada OOP
//Enkapsulasi
Enkapsulasi adalah sebuah fitur yang menggabungkan antara fungsionalitas dan data 
untuk menyembunyikan informasi. 
Enkapsulasi memungkinkan kita menggunakan fungsi dari sebuah objek tanpa perlu mengetahui detail 
dari apa yang terjadi dalam fungsi tersebut.

//Pewarisan
Pewarisan adalah sebuah fitur yang memungkinkan kita menggunakan fitur dari suatu class tanpa perlu 
mendefinisikan ulang semua method dan property yang ada pada class tersebut.

//Polimorfisme
Polimorfisme adalah sebuah fitur yang erat kaitannya dengan fitur sebelumnya yaitu pewarisan. 
Fitur ini secara mudah adalah sebuah kemampuan 
dari objek untuk merespon atau mengolah secara berbeda terhadap input yang sama.